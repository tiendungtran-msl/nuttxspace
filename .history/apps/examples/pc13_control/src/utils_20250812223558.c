/****************************************************************************
 * Public Functions
 ****************************************************************************/

void delay_ms(uint32_t ms)
{
  usleep(ms * 1000);
}

uint32_t get_timestamp(void)
{
  struct timeval tv;
  gettimeofday(&tv, NULL);
  return (uint32_t)(tv.tv_sec * 1000 + tv.tv_usec / 1000);
}